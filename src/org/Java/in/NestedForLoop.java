package org.Java.in;

public class NestedForLoop {
	public static void main(String[] args) {
		for(int i=0;i<2;i++) {
			for(int l=0;l<3;l++) {
				System.out.println("Inner Loop : "+l);
			}
		System.out.println("Outer Loop : "+i);
	}
	for(int a=3;a>=1;a--) {
		for(long b=0;b<3;b++) {
			System.out.println("Inner Loop : " +b);
		}System.out.println("Outer Loop : " + a);
	}
	}
	
}

	

